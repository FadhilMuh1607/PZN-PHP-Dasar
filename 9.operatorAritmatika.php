<?php 

$a = 16;
$b = 7;

$result = $a + $b;
var_dump($result);

// menjadikan variable result menjadi bilangan negatif
$resultNegative = -$result;
var_dump($resultNegative);

// menjadikan variable result menjadi bilangan positif kembali
$resultPositive = +$result;
var_dump($resultPositive);

// Operasi Modulo (Sisa bagi)
$resultModulo = 100 % 11;
var_dump($resultModulo);

// Operasi Perkalian
$resultPerkallian = 16 * 7;
var_dump($resultPerkallian);